#day_of_week.py

dof = int(input())

if dof == 1:
    print("Monday")
elif dof == 2:
    print("Tuesday")
elif dof == 3:
    print("Wednesday")
elif dof == 4:
    print("Thursday")
elif dof == 5:
    print("Friday")
elif dof == 6:
    print("Saturday")
elif dof == 7:
    print("Sunday")
else:
    print("error")